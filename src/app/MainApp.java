    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String userName = txtUsername.getText();
        String password = jPasswordField1.getText();
        
        program.logIn(userName, password);
        
        if(!program.isLoggedIn())
        {
            System.out.println("Could not login!");
            lblFelMeddelande.setText("Fel användarnamn eller lösenord!");
            return;
        }
        lblFelMeddelande.setText("");
            
        btnToAdmin.setVisible(program.isAdmin());
        if(program.isLoggedInAsAgent() && program.isAdmin())
        {
            showHome();
        }
        else if(program.isLoggedInAsAgent())
        {
            showAgentHome();
        }
        else if(program.isLoggedInAsAlien())
        {
            showAlienHome();
        }
        else
        {
            //Not logged in at all show error?
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

